#' Runs all of the dense matrix microbenchmarks
#'
#' \code{DenseMatrixBenchmark} runs all of the microbenchmarks for performance
#'   testing the dense matrix linear algebra kernels
#'
#' This function runs all of the dense matrix microbenchmarks defined in the
#' \code{microbenchmarks} input vector for which the \code{active} field is 
#' set to TRUE.  For each microbenchmark, it creates a
#' seperate output file in CSV format containing the performance results for
#' each matrix tested by the microbenchmark.  The names of the output files
#' follow the format \code{csvResultsBaseFileName}_\code{runIdentifier}.csv,
#' where \code{csvResultsBaseFileName} is specified in the
#' \code{DenseMatrixMicrobenchmark} object of each microbenchmark and
#' \code{runIdentifier} is an input parameter to this function.  The
#' \code{microbenchmarks} input vector
#' contains instances of the \code{DenseMatrixMicrobenchmark} class defining
#' each microbenchmark.  The default microbenchmarks are generated by the
#' function \code{\link{DenseMatrixDefaultMicrobenchmarks}}.  
#'
#' @param runIdentifier a character string specifying the suffix to be
#'   appended to the base of the file name of the output CSV format files
#' @param resultsDirectory a character string specifying the directory
#'   where all of the CSV performance results files will be saved
#' @param microbenchmarks a vector of \code{DenseMatrixMicrobenchmark} objects
#'   defining the microbenchmarks to execute as part of the dense matrix
#'   benchmark.  Default values are provided by the function
#'   \code{\link{DenseMatrixDefaultMicrobenchmarks}}.
#' @examples 
#' ## Run the default dense matrix benchmarks and place the results files
#' ## in the directory ./DenseMatrixResults with the run identifier test1
#' ## as a suffix to the base of the CSV file names
#' DenseMatrixBenchmark("test1", "./DenseMatrixResults")
#'
#' ## Run the default dense matrix benchmark with the QR decomposition
#' ## microbenchmark disabled
#' microbenchmarks <- DenseMatrixDefaultMicrobenchmarks()
#' microbenchmarks[["qr"]]$active <- FALSE
#' DenseMatrixBenchmark("test1", "./DenseMatrixResults")
#' @export
DenseMatrixBenchmark <- function(runIdentifier, resultsDirectory,
	microbenchmarks = DenseMatrixDefaultMicrobenchmarks()) {

   numberOfThreads <- strtoi(GetConfigurableEnvParameter("R_BENCH_NUM_THREADS_VARIABLE"))

   # Loop over all matrix kernel tests

   for (i in 1:length(microbenchmarks)) {
      if (microbenchmarks[[i]]$active) {
         microbenchmarkValue <- MicrobenchmarkDenseMatrixKernel(microbenchmarks[[i]], numberOfThreads, resultsDirectory, runIdentifier)
         invisible(gc())
      }
   }

}


